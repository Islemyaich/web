// Language management functions
function setLanguage(lang) {
  localStorage.setItem('language', lang);
  applyLanguage();
}

function getCurrentLanguage() {
  return localStorage.getItem('language') || 'en';
}

function applyLanguage() {
  const lang = getCurrentLanguage();
  document.documentElement.lang = lang;
  
  // Apply RTL for Arabic
  if (lang === 'ar') {
    document.documentElement.dir = 'rtl';
  } else {
    document.documentElement.dir = 'ltr';
  }
  
  // Translate all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    element.textContent = translations[lang][key] || translations['en'][key];
  });
  
  // Translate all input placeholders with data-translate-placeholder attribute
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    element.placeholder = translations[lang][key] || translations['en'][key];
  });
  
  // Translate all button values with data-translate-value attribute
  document.querySelectorAll('[data-translate-value]').forEach(element => {
    const key = element.getAttribute('data-translate-value');
    element.value = translations[lang][key] || translations['en'][key];
  });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  applyLanguage();
  
  // Set up language selector if it exists
  const languageSelector = document.getElementById('language-selector');
  if (languageSelector) {
    languageSelector.value = getCurrentLanguage();
    languageSelector.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
});